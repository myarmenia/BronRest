            6.9. Денежные средства, оплаченные за Бронирование c предзаказом Товара, не
            подлежат возврату Пользователю, если:
         </p>
         <p class="text__text">
            6.9.1 Пользователь не забрал Товар в период времени, выбранный на Сервисе
            для его получения посредством самовывоза.
         </p>
         <p class="text__text">
            6.9.2 Пользователь отказывается от Заказа после присвоения ему статуса “Готов”
            или “Завершен”.
         </p>
         <p class="text__text">
            6.10 Денежные средства подлежат возврату, если Пользователь отменил Заказ до
            присвоения ему статуса “Готов”.
         </p>

