        <h3 class="title033">Правила сотрудничества с ресторанами</h3>

        <p class="text__text">
            Версия 1.0 от 1.06.2022
        </p>
        <p class="text__text">
            Taptable — это информационная система для бронирования столиков в
            ресторанах с возможностью предзаказа блюд.
        </p>
        <p class="text__text">
            Термины и определения
        </p>
        <p class="text__text">
            <b>Сервис</b> - мобильное приложение “Taptable” для смартфонов, планшетов и других
мобильных устройств, предоставляющие пользователю Сервиса возможность
осуществить поиск и бронирование стола в ресторане c возможностью предзаказа
блюд на условиях, предусмотренных в Пользовательском соглашении. Мобильное
приложение доступно для скачивания в App Store, Google Play.
        </p>
        <p class="text__text">
            <b>Пользователь</b> - физическое лицо, которое оформило Бронирование через
            Сервис.
        </p>
        <p class="text__text">
            <b>Бронирование</b> - оформленный с помощью Сервиса запрос на бронирование
стола в ресторане.
        </p>
        <p class="text__text">
            <b>Ресторан</b> - юридическое лицо или индивидуальный предприниматель, который
оказывает услуги общественного питания посредством производства и
реализации товара.
        </p>
        <p class="text__text">
            <b>Товар</b> - пищевая продукция и/или напитки (за исключением алкогольных),
            предоплата которых осуществляется по меню, представленным в Сервисе.
        </p>
        <p class="text__text">
            <b>Личный кабинет</b> - персональный раздел в Сервисе, доступный при
            использовании логина и пароля.
        </p>
        <p class="text__text">
            <b>Контент</b> - изображения, текстовые, аудио- и видеоматериалы, а также иные
            объекты авторских и (или) смежных прав, а равно не являющиеся таковыми,
            информация и сообщения любого характера, доступные в Сервисе..
        </p>

